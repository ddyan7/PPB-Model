// Model provenance/metrics are static, so they're served from docs/model_info.json
// (generated by scripts/export_model_info.py) rather than the API - the panel renders
// instantly and doesn't depend on the Container App being awake.
async function loadModelInfo() {
  try {
    const r = await fetch("model_info.json");
    if (!r.ok) return;
    const m = await r.json();
    $("modelinfo").innerHTML = `
      <p><strong>Target:</strong> ${m.target}</p>
      <p><strong>Method:</strong> ${m.method}</p>
      <p><strong>Training data:</strong> ${m.training_data} - ${m.n_train} molecules.</p>
      ${perfChart(m.metrics, m.test_set, m.conformal_coverage)}
      <p><a href="${API_BASE}/docs" target="_blank" rel="noopener">REST API documentation</a>
         · Code: ${m.license_code} · Data: ${m.license_data}</p>`;
    renderParity();  // fills the #parity container from the held-out test points
    const repoLink = /github\.com\/.+/.test(m.repo)
      ? ` · <a href="${m.repo}" target="_blank" rel="noopener">Source code</a>` : "";
    const credits = (m.attribution || []).map(a => `<li>${a}</li>`).join("");
    $("provenance").innerHTML =
      `${m.citation}${repoLink}` +
      (credits ? `<ul class="credits">${credits}</ul>` : "");
  } catch (e) { /* non-fatal */ }
}

// Performance layout: the predicted-vs-observed parity plot (filled by
// renderParity) plus a one-line error summary. Three metrics don't warrant a
// chart, so they're stated inline.
function perfChart(t, testSet, calib) {
  return `
    <div class="perf">
      <h4>Predicted vs. observed, log fraction unbound - held-out ${testSet}</h4>
      <div id="parity"></div>
      <p class="sub">Held-out error (% bound): MAE ${t.MAE}, RMSE ${t.RMSE},
         Spearman ρ ${t.Spearman}. High-binding (≥90%) MAE ${t.high_binding_MAE}.
         Calibration ${calib}.</p>
    </div>`;
}

// Draw a parity (predicted-vs-observed) scatter from the real test points.
// Colours follow the page text (currentColor) so it works in light/dark.
async function renderParity() {
  const el = $("parity");
  if (!el) return;
  let data;
  try {
    const r = await fetch("parity_test.json");
    if (!r.ok) return;
    data = await r.json();
  } catch (e) { return; }

  // Plot in log10(fraction unbound): fu = (100 - PPB)/100. This is the standard
  // PPB space - it spreads the dense high-binding region along the diagonal
  // instead of crushing it into a corner. More negative = more strongly bound.
  const logfu = ppb => Math.log10(Math.min(1, Math.max(1e-4, (100 - ppb) / 100)));
  const lo = data.points.map(([o]) => logfu(o));
  const lp = data.points.map(([, p]) => logfu(p));
  const axMin = Math.max(-4, Math.floor(Math.min(...lo, ...lp)));  // e.g. -3

  const W = 470, H = 360, ml = 50, mr = 14, mt = 14, mb = 46;
  const pw = W - ml - mr, ph = H - mt - mb;
  const sx = v => ml + ((v - axMin) / (0 - axMin)) * pw;   // axMin→left, 0→right
  const sy = v => mt + (1 - (v - axMin) / (0 - axMin)) * ph; // 0→top, axMin→bottom
  const ticks = [];
  for (let t = axMin; t <= 0; t++) ticks.push(t);

  const grid = ticks.map(t => `
    <line x1="${sx(t)}" y1="${mt}" x2="${sx(t)}" y2="${mt + ph}" class="grid"/>
    <line x1="${ml}" y1="${sy(t)}" x2="${ml + pw}" y2="${sy(t)}" class="grid"/>
    <text x="${sx(t)}" y="${mt + ph + 16}" text-anchor="middle" class="ax">${t}</text>
    <text x="${ml - 8}" y="${sy(t) + 4}" text-anchor="end" class="ax">${t}</text>`).join("");
  // Opacity encodes distance from training data (max similarity): points less
  // similar to the training set are fainter, so "how far out of domain" is visible
  // continuously, while hue (blue/orange) still marks the in/out AD decision.
  const pts = data.points.map(([o, p, ad, sim]) => {
    const op = Math.max(0.2, Math.min(0.85, 0.2 + 0.65 * (sim ?? 0)));
    return `<circle cx="${sx(logfu(p)).toFixed(1)}" cy="${sy(logfu(o)).toFixed(1)}" r="3" ` +
           `class="pt${ad ? "" : " out"}" style="fill-opacity:${op.toFixed(2)}"/>`;
  }).join("");
  const m = data.metrics;

  el.innerHTML = `
  <svg viewBox="0 0 ${W} ${H}" class="parity" role="img"
       aria-label="Predicted versus observed log fraction-unbound parity plot on the held-out test set">
    <style>
      .parity .grid { stroke: currentColor; stroke-opacity: .12; }
      .parity .ax { fill: currentColor; font-size: 11px; opacity: .8; }
      .parity .axlab { fill: currentColor; font-size: 12px; font-weight: 600; }
      .parity .ident { stroke: #dc2626; stroke-width: 2; stroke-dasharray: 6 5; }
      .parity .pt { fill: #2563eb; fill-opacity: .5; }
      .parity .pt.out { fill: #f59e0b; fill-opacity: .65; }
      .parity .legbox { fill: currentColor; fill-opacity: .06; stroke: currentColor; stroke-opacity: .25; }
    </style>
    ${grid}
    <rect x="${ml}" y="${mt}" width="${pw}" height="${ph}" fill="none" stroke="currentColor" stroke-opacity=".3"/>
    <line x1="${sx(axMin)}" y1="${sy(axMin)}" x2="${sx(0)}" y2="${sy(0)}" class="ident"/>
    ${pts}
    <text class="axlab" x="${ml + pw / 2}" y="${H - 8}" text-anchor="middle">Predicted log₁₀(fu)</text>
    <text class="axlab" transform="translate(13 ${mt + ph / 2}) rotate(-90)" text-anchor="middle">Observed log₁₀(fu)</text>
    <rect class="legbox" x="${ml + pw - 150}" y="${mt + ph - 58}" width="140" height="50" rx="6"/>
    <text class="ax" x="${ml + pw - 140}" y="${mt + ph - 38}">R² = ${m.R2_logfu}</text>
    <text class="ax" x="${ml + pw - 140}" y="${mt + ph - 20}">RMSE = ${m.RMSE_logfu} log units</text>
  </svg>
  <p class="sub" style="margin-top:.3rem">n=${data.n} held-out test, log₁₀(fu).
     Toward -4 (bottom-left) = more strongly bound. Dashed = perfect prediction. Orange =
     out-of-domain (${m.n_out}, they scatter more, MAE ${m.MAE_out} vs ${m.MAE_in}).
     Fainter = less similar to training.</p>`;
}
