// ---- Prediction ---------------------------------------------------------------
$("go").addEventListener("click", async () => {
  const smiles = $("input").value.split("\n").map(s => s.trim()).filter(Boolean);
  $("error").textContent = "";
  clearResults();
  if (!smiles.length) { $("error").textContent = "Enter at least one SMILES."; return; }

  $("go").disabled = true;
  $("status").textContent = "Predicting…";
  try {
    const resp = await fetch(api("/api/predict"), {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ smiles }),
    });
    if (!resp.ok) {
      const body = await resp.json().catch(() => ({}));
      throw new Error(body.detail || `Request failed (${resp.status})`);
    }
    const data = await resp.json();
    window.__lastRows = data.predictions;
    render(data.predictions);
    $("status").textContent = "";
    $("status2").textContent = `${data.count} prediction(s)`;
  } catch (e) {
    $("error").textContent = e.message;
    $("status").textContent = "";
  } finally {
    $("go").disabled = false;
  }
});

function clearResults() {
  $("results").innerHTML = "";
  $("toolbar").style.display = "none";
  $("legend").style.display = "none";
  $("status2").textContent = "";
}

function fmt(x, digits) {
  return (x === null || x === undefined) ? "-" : Number(x).toFixed(digits);
}
// fraction (0-1) to percent, preserving null so fmt() can show a dash.
function pct(x) { return (x === null || x === undefined) ? null : x * 100; }

function band(v) {
  if (v === null || v === undefined) return "";
  if (v >= 90) return `<span class="pill b-high">High</span>`;
  if (v >= 70) return `<span class="pill b-mod">Moderate</span>`;
  return `<span class="pill b-low">Low</span>`;
}
function domainCell(row) {
  if (row.status !== "ok") return `<span class="pill bad">error</span>`;
  return row.in_applicability_domain
    ? `<span class="pill in">yes</span>`
    : `<span class="pill out">no</span>`;
}
function structCell(row) {
  return row.svg ? `<td class="struct">${row.svg}</td>` : `<td class="struct">-</td>`;
}

function render(rows) {
  const head = `<tr>
    <th title="2D depiction of the standardised molecule">Structure</th>
    <th title="Input SMILES">SMILES</th>
    <th title="Predicted percent bound to plasma proteins (0-100%)">PPB (% bound)</th>
    <th title="Free, pharmacologically active fraction = 100 - PPB">Unbound (%)</th>
    <th title="Molecular weight (g/mol)">MW</th>
    <th class="driver" title="Calculated lipophilicity (Crippen MolLogP) - the dominant PPB driver">LogP</th>
    <th title="Topological polar surface area (Å²)">TPSA</th>
    <th title="Max Tanimoto similarity (0-1) to any training molecule">Max train similarity</th>
    <th title="Within the model's applicability domain?">In domain?</th>
    <th title="SMILES standardisation result">Status</th></tr>`;
  const body = rows.map(r => `<tr>
    ${structCell(r)}
    <td class="smiles" title="${escapeHtml(r.input_smiles)}">${escapeHtml(r.input_smiles)}</td>
    <td class="num">${fmt(r.predicted_ppb, 1)} ${band(r.predicted_ppb)}</td>
    <td class="num">${fmt(pct(r.predicted_fraction_unbound), 1)}</td>
    <td class="num">${fmt(r.mw, 1)}</td>
    <td class="num driver">${fmt(r.logp, 2)}</td>
    <td class="num">${fmt(r.tpsa, 1)}</td>
    <td class="num">${fmt(r.max_training_similarity, 2)}</td>
    <td>${domainCell(r)}</td>
    <td>${escapeHtml(r.status)}</td></tr>`).join("");
  $("results").innerHTML = `<table>${head}${body}</table>`;
  $("toolbar").style.display = "flex";
  $("legend").style.display = "block";
}

// ---- CSV export ---------------------------------------------------------------
// One source of truth for CSV column names and their definitions (the same
// meanings shown as table tooltips / the on-page legend). ASCII-only so the
// comment header stays clean in Excel and other CSV readers.
const COL_DEFS = {
  input_smiles: "The SMILES you submitted",
  predicted_ppb: "Predicted percent bound to plasma proteins (0-100)",
  unbound_percent: "Free, pharmacologically active fraction = 100 - PPB",
  mw: "Molecular weight (g/mol)",
  logp: "Calculated lipophilicity (Crippen MolLogP); the dominant PPB driver",
  tpsa: "Topological polar surface area (Angstrom^2)",
  max_training_similarity: "Max Tanimoto similarity (0-1) to any training molecule",
  in_applicability_domain: "Within the applicability domain? predictions outside are unreliable",
  status: "SMILES standardisation result (ok / error code)",
};

$("csv").addEventListener("click", () => {
  const rows = window.__lastRows;
  if (!rows || !rows.length) return;
  const cols = Object.keys(COL_DEFS);
  const cell = (v) => {
    const s = (v === null || v === undefined) ? "" : String(v);
    return /[",\n]/.test(s) ? `"${s.replace(/"/g, '""')}"` : s;
  };
  const lines = rows.map(r => {
    const v = {
      input_smiles: r.input_smiles,
      predicted_ppb: r.predicted_ppb == null ? "" : r.predicted_ppb.toFixed(2),
      unbound_percent: r.predicted_fraction_unbound == null ? "" : (r.predicted_fraction_unbound * 100).toFixed(1),
      mw: r.mw ?? "", logp: r.logp ?? "", tpsa: r.tpsa ?? "",
      max_training_similarity: r.max_training_similarity == null ? "" : r.max_training_similarity.toFixed(3),
      in_applicability_domain: r.in_applicability_domain,
      status: r.status,
    };
    return cols.map(c => cell(v[c])).join(",");
  });
  // Prepend '#'-prefixed column definitions (pandas: read_csv(comment='#')).
  const legend = cols.map(c => `# ${c}: ${COL_DEFS[c]}`).join("\n");
  const csv = legend + "\n" + cols.join(",") + "\n" + lines.join("\n");
  const a = document.createElement("a");
  a.href = URL.createObjectURL(new Blob([csv], { type: "text/csv" }));
  a.download = "ppb_predictions.csv";
  a.click();
  URL.revokeObjectURL(a.href);
});

function escapeHtml(s) {
  return String(s).replace(/[&<>"']/g, c =>
    ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" }[c]));
}
